public class Variables {

	public static void main (String[] args) {
		int x = 123;
		float y = 225.58f;
		double z = 25.58;
		char a = 'A';
		boolean b = true;
		String c = "Hello";
		
		System.out.println(x);
		System.out.println(y);
		System.out.println(z);
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(a + " " + c);


		System.out.println(x);

}
}
